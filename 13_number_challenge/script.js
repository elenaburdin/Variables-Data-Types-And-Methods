let x = Math.floor(Math.random() * 100 + 1);
let y = Math.floor(Math.random() * 50 + 1);

// Get the sum
let sumOutput = `${x} + ${y} = ${x + y}`
console.log(`sumOutput => ${sumOutput}`);

// Get the difference
let differenceOutput = `${x} - ${y} = ${x - y}`;
console.log(`differenceOutput => ${differenceOutput}`);

// Get the product
let productOutput = `${x} * ${y} = ${x * y}`;
console.log(`productOutput => ${productOutput}`);

// Get the quotient
let quotientOutput = `${x} / ${y} = ${x / y}`;
console.log(`quotientOutput => ${quotientOutput}`);

// Get the remainder
let rmOutput = `${x} % ${y} = ${x % y}`;
console.log(`rmOutput => ${rmOutput}`);